package piscine

func ActiveBits(n int) int {
	// 1. Преобразуем знаковое число (int) в беззнаковое (uint).
	// Это критически важно для безопасной работы с отрицательными числами.
	u := uint(n)

	count := 0

	// 2. Выполняем цикл, пока в числе не останутся только нули
	for u != 0 {
		// 3. Проверяем самый правый (младший) бит
		if u&1 == 1 {
			count++
		}
		// 4. Сдвигаем все биты вправо на 1 позицию
		u >>= 1
	}

	return count
}
