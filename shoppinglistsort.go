package piscine

func ShoppingListSort(slice []string) []string {
	// Сохраняем длину массива в переменную для удобства
	n := len(slice)

	// Внешний цикл отвечает за количество проходов по массиву
	for i := 0; i < n-1; i++ {
		// Внутренний цикл сравнивает соседние элементы
		for j := 0; j < n-i-1; j++ {
			// ВНИМАНИЕ: Сравниваем длину строк, а не сами строки!
			if len(slice[j]) > len(slice[j+1]) {
				// Если текущее слово длиннее следующего, меняем их местами
				slice[j], slice[j+1] = slice[j+1], slice[j]
			}
		}
	}

	// Возвращаем отсортированный срез
	return slice
}
