package piscine

func Unmatch(a []int) int {
	// 1. Создаем карту для подсчета вхождений каждого числа
	counts := make(map[int]int)

	// 2. Считаем, сколько раз каждое число встречается в массиве
	for _, n := range a {
		counts[n]++
	}

	// 3. Проходим по массиву еще раз, чтобы найти первое число с нечетным количеством вхождений
	for _, n := range a {
		if counts[n]%2 != 0 {
			return n
		}
	}

	// 4. Если таких чисел нет, возвращаем -1
	return -1
}
