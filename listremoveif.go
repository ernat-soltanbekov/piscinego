package piscine

func ListRemoveIf(l *List, data_ref interface{}) {
	// Базовая защита: если список вообще не существует или пуст, ничего не делаем
	if l == nil || l.Head == nil {
		return
	}

	// 1. Обрабатываем НАЧАЛО списка
	// Если первые элементы совпадают с data_ref, мы просто сдвигаем Head вперед.
	// Используем for, так как в начале может быть сразу несколько совпадающих элементов.
	for l.Head != nil && l.Head.Data == data_ref {
		l.Head = l.Head.Next
	}

	// Если после удаления из начала список оказался полностью пустым
	// (например, если все элементы в списке равнялись data_ref)
	if l.Head == nil {
		l.Tail = nil // Не забываем обнулить хвост!
		return
	}

	// 2. Обрабатываем СЕРЕДИНУ и КОНЕЦ списка
	current := l.Head
	for current.Next != nil {
		if current.Next.Data == data_ref {
			// Если следующий узел нужно удалить, мы просто "перепрыгиваем" его,
			// заставляя текущий узел указывать на один шаг дальше.
			current.Next = current.Next.Next
		} else {
			// Если удалять не нужно, шагаем вперед
			current = current.Next
		}
	}

	// 3. Обновляем ХВОСТ (Tail)
	// Поскольку мы дошли до конца списка в цикле выше (когда current.Next == nil),
	// переменная current сейчас гарантированно указывает на последний валидный элемент.
	l.Tail = current
}
