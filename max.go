package piscine

func Max(a []int) int {
	// 1. Проверяем граничное условие: если срез пустой
	if len(a) == 0 {
		return 0
	}

	// 2. Берем первый элемент как временный максимум
	maxVal := a[0]

	// 3. Проходим циклом по всем элементам среза
	// Можно начать со второго элемента (индекс 1), так как первый мы уже учли
	for i := 1; i < len(a); i++ {
		// Если текущий элемент больше нашего "временного максимума"
		if a[i] > maxVal {
			// Обновляем максимум
			maxVal = a[i]
		}
	}

	// 4. Возвращаем найденное максимальное значение
	return maxVal
}
