package piscine

func CollatzCountdown(start int) int {
	// Если стартовое число 0 или меньше, возвращаем -1 по условию
	if start <= 0 {
		return -1
	}

	// Переменная для подсчета количества шагов
	steps := 0

	// Цикл работает до тех пор, пока число не достигнет 1
	for start > 1 {
		if start%2 == 0 {
			// Если четное — делим на 2
			start = start / 2
		} else {
			// Если нечетное — умножаем на 3 и прибавляем 1
			start = 3*start + 1
		}
		// Увеличиваем счетчик шагов на 1 после каждой операции
		steps++
	}
	return steps
}
