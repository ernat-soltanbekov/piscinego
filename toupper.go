package piscine

func ToUpper(s string) string {
	// Преобразуем строку в слайс рун для изменения символов
	runes := []rune(s)

	for i, r := range runes {
		// Проверяем, является ли символ строчной латинской буквой
		if r >= 'a' && r <= 'z' {
			// Превращаем в заглавную, вычитая 32 из кода символа
			// ('a' = 97, 'A' = 65, разница = 32)
			runes[i] = r - 32
		}
	}

	// Возвращаем результат, превратив руны обратно в строку
	return string(runes)
}
