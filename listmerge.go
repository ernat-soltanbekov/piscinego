package piscine

func ListMerge(l1 *List, l2 *List) {
	// Базовая проверка: если второго списка нет или он пустой, то и объединять нечего
	if l1 == nil || l2 == nil || l2.Head == nil {
		return
	}

	// Если первый список пустой, то результатом объединения станет просто второй список
	if l1.Head == nil {
		l1.Head = l2.Head
		l1.Tail = l2.Tail
		return
	}

	// Главная магия: соединяем хвост первого списка с головой второго списка
	l1.Tail.Next = l2.Head

	// Обновляем указатель хвоста первого списка — теперь его концом становится хвост второго списка
	l1.Tail = l2.Tail
}
