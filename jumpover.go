package piscine

func JumpOver(str string) string {
	// Превращаем строку в срез рун ([]rune), чтобы корректно работать
	// с любыми символами, включая кириллицу или эмодзи.
	runes := []rune(str)
	result := ""

	// Цикл начинается с индекса 2 (это 3-й по счету символ)
	// и на каждом шаге увеличивает i на 3 (прыгает через 3 символа)
	for i := 2; i < len(runes); i += 3 {
		result += string(runes[i])
	}

	// По условию задачи, результат всегда должен заканчиваться на \n
	result += "\n"

	return result
}
