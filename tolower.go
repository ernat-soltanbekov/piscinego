package piscine

func ToLower(s string) string {
	// Превращаем строку в массив рун, чтобы иметь возможность менять символы
	runes := []rune(s)

	for i, r := range runes {
		// Проверяем, является ли символ заглавной латинской буквой
		if r >= 'A' && r <= 'Z' {
			// Превращаем в строчную, прибавляя 32 к коду символа
			// ('A' = 65, 'a' = 97, разница = 32)
			runes[i] = r + 32
		}
	}

	// Возвращаем результат как строку
	return string(runes)
}
