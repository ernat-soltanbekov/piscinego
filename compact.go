package piscine

func Compact(ptr *[]string) int {
	// 1. Разыменовываем указатель, чтобы получить сам срез для удобной работы
	slice := *ptr

	// Переменная count будет отслеживать индекс для записи непустых строк,
	// а также служить итоговым размером нового среза.
	count := 0

	// 2. Проходим по всем элементам исходного среза
	for i := 0; i < len(slice); i++ {
		// Если строка не является пустой (не zero-value)
		if slice[i] != "" {
			// Перемещаем её на позицию count и увеличиваем count
			slice[count] = slice[i]
			count++
		}
	}

	// 3. Обновляем оригинальный срез по указателю.
	// Мы "обрезаем" его до нового размера count.
	*ptr = slice[:count]

	// 4. Возвращаем количество непустых элементов
	return count
}
